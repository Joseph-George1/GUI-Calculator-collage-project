    public void AO(){
        switch(calc)
        {
            case 1:
                sum = num + Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(sum));
                break;
            case 2:
                sum = num * Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(sum));
                break;

            case 3:
                sum = num / Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(sum));
                break;
            case 4:
                sum = num - Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(sum));
                break;                
                
                
        }
            
    }

